pub fn keySymToUnicode(keysym: c_ulong) ?u21 {
    const value: u32 = std.math.cast(u32, keysym) orelse return null;

    if ((value >= 0x0020 and value <= 0x007e) or
        (value >= 0x00a0 and value <= 0x00ff))
    {
        return @intCast(value);
    }

    if ((value & 0xff000000) == 0x01000000) {
        const codepoint = value & 0x00ffffff;
        if (codepoint <= 0x10ffff) return @intCast(codepoint);
        return null;
    }

    var min: usize = 0;
    var max: usize = keysymtab.len;
    while (min < max) {
        const mid = min + (max - min) / 2;
        if (keysymtab[mid].keysym < value) {
            min = mid + 1;
        } else if (keysymtab[mid].keysym > value) {
            max = mid;
        } else {
            return keysymtab[mid].ucs;
        }
    }

    return null;
}

test "keysym unicode conversion matches GLFW fast paths" {
    try std.testing.expectEqual(@as(?u21, 0x41), keySymToUnicode(0x0041));
    try std.testing.expectEqual(@as(?u21, 0x20ac), keySymToUnicode(0x20ac));
    try std.testing.expectEqual(@as(?u21, 0x1f600), keySymToUnicode(0x0101f600));
    try std.testing.expectEqual(@as(?u21, null), keySymToUnicode(0xffff));
}
